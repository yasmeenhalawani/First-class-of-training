
#What are complex numbers? 
# Complex numbers consist of two parts, real and imaginary part.

# root of -1 in python represented by a letter j
#To create a complex number
c = 2 + 4j

# THis would be a complex number

# To get the imaginary part of this variable
print(c.imag) # -> 4

# TO get the real part of this number 
print(c.real) # -> 2